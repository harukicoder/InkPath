#!/usr/bin/env node
/**
 * Generates data/daily-news.js — a short brief on current events + economic
 * news in China, written with HSK ≤ 4 vocabulary. Invoked daily by the
 * `.github/workflows/daily-news.yml` workflow.
 *
 * Reads:  GEMINI_API_KEY env var (required)
 * Writes: data/daily-news.js (overwritten in place)
 */

import { GoogleGenerativeAI } from "@google/generative-ai";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const OUT = path.resolve(__dirname, "..", "data", "daily-news.js");

if (!process.env.GEMINI_API_KEY) {
  console.error("GEMINI_API_KEY is not set.");
  process.exit(1);
}

const genAI = new GoogleGenerativeAI(process.env.GEMINI_API_KEY);
const model = genAI.getGenerativeModel({ 
  model: "gemini-1.5-flash",
  generationConfig: {
    responseMimeType: "application/json",
  }
});

const today = new Date().toISOString().slice(0, 10);

const SCHEMA_EXAMPLE = {
  date: today,
  title: { hz: "…", py: "…", en: "…" },
  description: "AI-generated news + economy brief · HSK ≤ 4",
  sentences: [
    {
      en: "English gloss of the sentence.",
      words: [
        { hz: "今天", py: "jīntiān", en: "today" },
        { hz: "天气", py: "tiānqì", en: "weather" },
        { hz: "很好。", py: "hěn hǎo", en: "is great" }
      ]
    }
  ]
};

const SYSTEM = `You are a language educator producing a short daily news brief in Simplified Chinese for learners at HSK level 4 or below.

Hard rules:
- Use only vocabulary and grammar within HSK 1–4. Avoid idioms, chengyu, and advanced connectors.
- Cover 2–3 real recent items about China: at least one general news item and at least one economic / business item.
- Neutral tone. Factual. No editorializing.
- 10–15 sentences total. Each sentence must be one complete idea with a terminal punctuation mark.
- Return one JSON object exactly matching the provided schema.
- Word segmentation: split each sentence into meaningful words (not single chars unless they are standalone words). Each word is an object with hz (the Chinese surface form, punctuation appended to the last word of a sentence), py (pinyin with tone marks, lowercase, words separated by spaces only within multi-syllable words), en (concise gloss).`;

const USER = `Today is ${today}. Write the brief for today.

JSON schema (example — your output must match this shape):
${JSON.stringify(SCHEMA_EXAMPLE, null, 2)}`;

function validate(brief) {
  if (!brief || typeof brief !== "object") throw new Error("not an object");
  if (!brief.title || !brief.title.hz) throw new Error("missing title.hz");
  if (!Array.isArray(brief.sentences) || brief.sentences.length < 5)
    throw new Error("too few sentences");
  for (const s of brief.sentences) {
    if (!Array.isArray(s.words) || !s.words.length) throw new Error("sentence missing words");
    for (const w of s.words) {
      if (typeof w.hz !== "string" || !w.hz.length) throw new Error("word missing hz");
      if (typeof w.py !== "string") throw new Error("word missing py");
      if (typeof w.en !== "string") throw new Error("word missing en");
    }
  }
  return true;
}

const result = await model.generateContent({
  contents: [
    { role: "user", parts: [{ text: SYSTEM + "\n\n" + USER }] }
  ]
});

const text = result.response.text();
let brief;
try {
  brief = JSON.parse(text);
} catch (e) {
  console.error("Failed to parse Gemini response as JSON:", text);
  process.exit(1);
}

brief.date = today;
validate(brief);

const out = `// Auto-generated by scripts/generate-daily-news.mjs on ${today}.
// Do not edit by hand — regenerated daily by the daily-news workflow.
window.DAILY_NEWS = ${JSON.stringify(brief, null, 2)};
`;

fs.writeFileSync(OUT, out, "utf8");
console.log("Wrote", OUT, "(" + brief.sentences.length + " sentences)");
